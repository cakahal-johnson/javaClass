    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText().toString();
        String password = jPasswordField1.getText().toString();
        String about = jTextArea1.getText().toString();
        
        String gender = "";
        if(jRadioButton1.isSelected()){
            gender = "Male";
        }else{
            gender = "Femail";
        }
        
        String hubby = "";
        if(jCheckBox1.isSelected() && jCheckBox2.isSelected()){
            hubby = jCheckBox1.getLabel()+" "+jCheckBox2.getLabel();
        }else if(jCheckBox1.isSelected()){
            hubby = jCheckBox1.getLabel();
        }else if(jCheckBox2.isSelected()){
            hubby = jCheckBox2.getLabel();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Please select a hubby");
            return;
        }
        
        String qualification = jComboBox1.getSelectedItem().toString();
        
        /////Form validation///
        
        if(username.trim().length() < 2){
            JOptionPane.showMessageDialog(rootPane, "Username must be more than 2");
            return;
        }
        
        if(password.trim().length() < 6){
            JOptionPane.showMessageDialog(rootPane, "Password must be more than 6");
            return;
        }
        
        if(about.trim().length() < 10){
            JOptionPane.showMessageDialog(rootPane, "About must be more than 2");
            return;
        }
        
        
        /////The DATABASE SECTION for pop up JOptionPane/////
        String result = "Success!!! \n Your Details: \n"+
                "Username: "+username+
                "\n Password: "+password+
                "\n Gender: "+gender+
                "\n Qualification: "+qualification+
                "\n Hubby: "+hubby+
                "\n About Me:"+about;
        
         JOptionPane.showMessageDialog(rootPane, result);

        
  
    }//GEN-LAST:event_jToggleButton1ActionPerformed

