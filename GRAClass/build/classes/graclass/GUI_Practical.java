    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String username = jTextField1.getText().toString();
        String password = jPasswordField2.getText().toString();
        String comment = jTextArea1.getText().toString();
        
        String gender = "";
        if (jRadioButton3.isSelected()) {
            gender = "Male";
        }else if (jRadioButton4.isSelected()) {
            gender = "Female";
        }else{
            gender = "Others";
        }
        
        String hubby = "";
        if (jCheckBox1.isSelected() && jCheckBox2.isSelected() && jCheckBox3.isSelected()) {
            hubby = jCheckBox1.getLabel()+ ": "+jCheckBox2.getLabel()+ ": "+jCheckBox3.getLabel();
        }else if (jCheckBox1.isSelected()) {
            hubby = jCheckBox1.getLabel();
        }else if (jCheckBox2.isSelected()) {
            hubby = jCheckBox2.getLabel();
        }else if (jCheckBox3.isSelected()) {
            hubby = jCheckBox3.getLabel();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Please select a hubby");
            return;
        }
        
        String qualification = jComboBox1.getSelectedItem().toString();
        
        ///Form Validation///
        
        if (username.trim().length() < 2) {
            JOptionPane.showMessageDialog(rootPane, "Opps! Username must be more than 2");
            return;
        }
        
        if(password.trim().length() < 6){
            JOptionPane.showMessageDialog(rootPane, "Opps! password must be more than 6");
            return;
        }
        
        if (comment.trim().length() < 1) {
            JOptionPane.showMessageDialog(rootPane, "Opps! comments can not be empty");
            return;
        }
        
        
//        here we print out the result with our JOptionPane
        
        String result = "Success! \n Here is You Login Details \n"+
                "Username: "+username+
                "\n Password: "+password+
                "\n Gender: "+gender+
                "\n Qualification: "+qualification+
                "\n Hubby: "+hubby+
                "\n Comment: "+comment;
        
        JOptionPane.showMessageDialog(rootPane, result);
        
    }//GEN-LAST:event_jButton1ActionPerformed

